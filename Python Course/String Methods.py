#String Methods Part One
name = "kaVya gAnDHi"

#len() function - It is Used to Count number of characters inside variable and strings.
print(len(name))
print(len("hello"))

#lower method - It is Used to make all the characters small inside string or variable .
print("KAVyA".lower())
print(name.lower())

#upper method - It is Used to make all the character big inside string or variable .
print("kaVya".upper())
print(name.upper())

#title method - It is Used to make the first character of word biginside string or variable .
print("kAygkK;".title())
print(name.title())

#count method - 
print(name.title().count('a'))